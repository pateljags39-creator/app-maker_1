// Constants for precision and mathematical conversions
const PRECISION_INTERNAL = 14; // Internal calculation precision for toFixed
const PRECISION_DISPLAY = 10; // Maximum decimal places for displayed results

/**
 * Converts degrees to radians.
 * @param {number} deg - Angle in degrees.
 * @returns {number} Angle in radians.
 */
const toRadians = (deg) => deg * (Math.PI / 180);

